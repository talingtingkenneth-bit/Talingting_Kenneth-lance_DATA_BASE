    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
// 1. Check if the user actually clicked on a row in the table first
int selectedRow = studentTable.getSelectedRow();

if (selectedRow == -1) {
    // If they didn't select anyone, show a warning and stop
    javax.swing.JOptionPane.showMessageDialog(this, "Please select a student from the table to delete!");
    return;
}

// 2. Ask for confirmation (Good practice so they don't delete someone by accident!)
int confirm = javax.swing.JOptionPane.showConfirmDialog(this, 
        "Are you sure you want to delete this student?", 
        "Confirm Delete", 
        javax.swing.JOptionPane.YES_NO_OPTION);

if (confirm == javax.swing.JOptionPane.YES_OPTION) {
    try {
        // 3. Grab the 'student_id' from the very first column (column 0) of the selected row
        int studentId = Integer.parseInt(studentTable.getValueAt(selectedRow, 0).toString());
        
        // 4. Connect to the database and write the DELETE SQL command
        java.sql.Connection conn = DBConnection.getConnection();
        String sql = "DELETE FROM students WHERE student_id = ?";
        java.sql.PreparedStatement pstmt = conn.prepareStatement(sql);
        
        // 5. Attach the ID to the command and execute it
        pstmt.setInt(1, studentId);
        pstmt.executeUpdate();
        
        // 6. Tell the user it worked!
        javax.swing.JOptionPane.showMessageDialog(this, "Student Deleted Successfully!");
        
        // 7. Refresh the table instantly so the student disappears from the screen
        loadStudentData();
        
    } catch (java.sql.SQLException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage());
    }
}        // TODO add your handling code here:
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // 1. Get the text that the user typed into the boxes
String firstName = txtFirstName.getText();
String lastName = txtLastName.getText();
String ageText = txtAge.getText();
String email = txtEmail.getText();

// 2. Check if any boxes are empty
if (firstName.isEmpty() || lastName.isEmpty() || ageText.isEmpty() || email.isEmpty()) {
    javax.swing.JOptionPane.showMessageDialog(this, "Please fill in all fields!");
    return; // Stop the code here if a box is empty
}

try {
    // 3. Convert the age text into an actual Number (Integer)
    int age = Integer.parseInt(ageText);
    
    // 4. Connect to the database and prepare the SQL command
    java.sql.Connection conn = DBConnection.getConnection();
    String sql = "INSERT INTO students (first_name, last_name, age, email) VALUES (?, ?, ?, ?)";
    java.sql.PreparedStatement pstmt = conn.prepareStatement(sql);
    
    // 5. Fill in the '?' placeholders with our data
    pstmt.setString(1, firstName);
    pstmt.setString(2, lastName);
    pstmt.setInt(3, age);
    pstmt.setString(4, email);
    
    // 6. Execute the command to save it!
    pstmt.executeUpdate();
    
    // 7. Show a success popup
    javax.swing.JOptionPane.showMessageDialog(this, "Student Added Successfully!");
    
    // 8. Clear the boxes for the next entry
    txtFirstName.setText("");
    txtLastName.setText("");
    txtAge.setText("");
    txtEmail.setText("");
    txtFirstName.requestFocus();
    loadStudentData();

} catch (NumberFormatException e) {
    // If they typed a word instead of a number for Age
    javax.swing.JOptionPane.showMessageDialog(this, "Error: Age must be a number!");
} catch (java.sql.SQLException e) {
    // If something goes wrong with the database
    javax.swing.JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage());
}
    }//GEN-LAST:event_btnAddActionPerformed

