    // --- METHOD TO LOAD DATA INTO THE TABLE ---
    private void loadStudentData() {
        try {
            // 1. Get the connection
            java.sql.Connection conn = DBConnection.getConnection();
            
            // 2. Prepare the SQL to select everything from the students table
            String sql = "SELECT * FROM students";
            java.sql.Statement stmt = conn.createStatement();
            
            // 3. Run the query and get the results
            java.sql.ResultSet rs = stmt.executeQuery(sql);
            
            // 4. Get the visual table's "Model" (this controls the rows and columns)
            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) studentTable.getModel();
            
            // 5. Wipe the table clean before loading new data so it doesn't duplicate
            model.setRowCount(0);
            
            // 6. Loop through every row in the database
            while (rs.next()) {
                int id = rs.getInt("student_id");
                String firstName = rs.getString("first_name");
                String lastName = rs.getString("last_name");
                int age = rs.getInt("age");
                String email = rs.getString("email");
                
                // 7. Add the row to our visual table
                model.addRow(new Object[]{id, firstName, lastName, age, email});
            }
            
        } catch (java.sql.SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error loading data: " + e.getMessage());
        }
    }
