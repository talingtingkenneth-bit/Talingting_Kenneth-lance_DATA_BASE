    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
// 1. Check if a row is actually selected
int selectedRow = studentTable.getSelectedRow();
if (selectedRow == -1) {
    javax.swing.JOptionPane.showMessageDialog(this, "Please select a student from the table to update!");
    return;
}

try {
    // 2. Get the hidden ID of the student we are updating (from column 0)
    int studentId = Integer.parseInt(studentTable.getValueAt(selectedRow, 0).toString());
    
    // 3. Get the NEW text that the user typed into the boxes
    String firstName = txtFirstName.getText();
    String lastName = txtLastName.getText();
    int age = Integer.parseInt(txtAge.getText());
    String email = txtEmail.getText();
    
    // 4. Connect to database and write the UPDATE SQL command
    java.sql.Connection conn = DBConnection.getConnection();
    String sql = "UPDATE students SET first_name=?, last_name=?, age=?, email=? WHERE student_id=?";
    java.sql.PreparedStatement pstmt = conn.prepareStatement(sql);
    
    // 5. Attach the new data to the command
    pstmt.setString(1, firstName);
    pstmt.setString(2, lastName);
    pstmt.setInt(3, age);
    pstmt.setString(4, email);
    pstmt.setInt(5, studentId); // This tells MySQL exactly WHICH student to update
    
    // 6. Execute the update!
    pstmt.executeUpdate();
    javax.swing.JOptionPane.showMessageDialog(this, "Student Updated Successfully!");
    
    // 7. Refresh the table and clear the boxes
    loadStudentData();
    txtFirstName.setText("");
    txtLastName.setText("");
    txtAge.setText("");
    txtEmail.setText("");
    
} catch (NumberFormatException e) {
    javax.swing.JOptionPane.showMessageDialog(this, "Error: Age must be a number!");
} catch (java.sql.SQLException e) {
    javax.swing.JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage());
}        // TODO add your handling code here:
    }//GEN-LAST:event_btnUpdateActionPerformed

